package random

import (
	"crypto/rand"
	"encoding/base64"
)

// GenerateRandomBytes generates a random byte slice of size n.
func GenerateRandomBytes(n int) ([]byte, error) {
	b := make([]byte, n)
	_, err := rand.Read(b)
	if err != nil {
		return nil, err
	}

	return b, nil
}

// GenerateRandomString generates a random string of a base64 encoded byte slice of size s.
func GenerateRandomString(s int) (string, error) {
	b, err := GenerateRandomBytes(s)
	return base64.URLEncoding.EncodeToString(b), err
}
